package ua.nure.serdyuk.exception;

/**
 * {@code ValidationException} occurs when incorrect/invalid data was entered by
 * user.
 * 
 * @author Daria Serdiuk
 *
 */
public class ValidationException extends AppException {

	private static final long serialVersionUID = -451315363147334886L;

	public ValidationException() {
		super();
	}

	public ValidationException(String message, Throwable cause) {
		super(message, cause);
	}

	public ValidationException(String message) {
		super(message);
	}

}
